package SistemaMilhas.model;



public class Milhas {

    private int quantidadePontosAlcancados;

    public Milhas(int quantidadePontosAlcancados) {
        this.quantidadePontosAlcancados = quantidadePontosAlcancados;
    }

    public int getQuantidadePontosAlcancados() {
        return quantidadePontosAlcancados;
    }

    public void setQuantidadePontosAlcancados(int quantidadePontosAlcancados) {
        this.quantidadePontosAlcancados = quantidadePontosAlcancados;
    }
}
